package id.co.telkomsigma.entity;

public class FloorPojo {
	private String buyPrice;
	private String buyLot;
	private String sellPrice;
	private String sellLot;
	
	public String getBuyPrice() {
		return buyPrice;
	}
	public void setBuyPrice(String buyPrice) {
		this.buyPrice = buyPrice;
	}
	public String getBuyLot() {
		return buyLot;
	}
	public void setBuyLot(String buyLot) {
		this.buyLot = buyLot;
	}
	public String getSellPrice() {
		return sellPrice;
	}
	public void setSellPrice(String sellPrice) {
		this.sellPrice = sellPrice;
	}
	public String getSellLot() {
		return sellLot;
	}
	public void setSellLot(String sellLot) {
		this.sellLot = sellLot;
	}
	
}
